public class BahanPokok {
    private String ayam,daunKunyit,daunJeruk,serai,lengkuas,gulaMerah,asamJawa,cengkih,
    kayuManis,santan;

    public BahanPokok() {
    }

    public BahanPokok(String ayam, String daunKunyit, String daunJeruk, String serai, String lengkuas, String gulaMerah, String asamJawa, String cengkih, String kayuManis, String santan) {
        this.ayam = ayam;
        this.daunKunyit = daunKunyit;
        this.daunJeruk = daunJeruk;
        this.serai = serai;
        this.lengkuas = lengkuas;
        this.gulaMerah = gulaMerah;
        this.asamJawa = asamJawa;
        this.cengkih = cengkih;
        this.kayuManis = kayuManis;
        this.santan = santan;
    }

    public String getAyam() {
        return ayam;
    }

    public void setAyam(String ayam) {
        this.ayam = ayam;
    }

    public String getDaunKunyit() {
        return daunKunyit;
    }

    public void setDaunKunyit(String daunKunyit) {
        this.daunKunyit = daunKunyit;
    }

    public String getDaunJeruk() {
        return daunJeruk;
    }

    public void setDaunJeruk(String daunJeruk) {
        this.daunJeruk = daunJeruk;
    }

    public String getSerai() {
        return serai;
    }

    public void setSerai(String serai) {
        this.serai = serai;
    }

    public String getLengkuas() {
        return lengkuas;
    }

    public void setLengkuas(String lengkuas) {
        this.lengkuas = lengkuas;
    }

    public String getGulaMerah() {
        return gulaMerah;
    }

    public void setGulaMerah(String gulaMerah) {
        this.gulaMerah = gulaMerah;
    }

    public String getAsamJawa() {
        return asamJawa;
    }

    public void setAsamJawa(String asamJawa) {
        this.asamJawa = asamJawa;
    }

    public String getCengkih() {
        return cengkih;
    }

    public void setCengkih(String cengkih) {
        this.cengkih = cengkih;
    }

    public String getKayuManis() {
        return kayuManis;
    }

    public void setKayuManis(String kayuManis) {
        this.kayuManis = kayuManis;
    }

    public String getSantan() {
        return santan;
    }

    public void setSantan(String santan) {
        this.santan = santan;
    }
}
